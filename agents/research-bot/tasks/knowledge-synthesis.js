'use strict';

const path = require('node:path');

const SYNTHESIS_PROMPT = `You are synthesizing research findings into insights.

Findings from the past week:
{findings}

Generate a research brief that:
1. Identifies key trends
2. Highlights important developments
3. Notes competitor movements
4. Recommends actions

Format as markdown with sections. Keep under 1000 words.`;

async function knowledgeSynthesisTask({ db, config, logger, llmTool, writeFileTool }) {
  if (!db) {
    return { ok: false, error: 'no_db' };
  }

  // Get recent findings
  const findingsRows = db.all(
    `SELECT value FROM kv_store 
     WHERE namespace = 'research_findings'
     AND datetime(created_at) >= datetime('now', '-7 days')
     ORDER BY created_at DESC
     LIMIT 50`
  );

  const competitorRows = db.all(
    `SELECT value FROM kv_store 
     WHERE namespace = 'competitor_intel'
     AND datetime(created_at) >= datetime('now', '-7 days')`
  );

  if (findingsRows.length === 0 && competitorRows.length === 0) {
    if (logger) logger.info('knowledge_synthesis_skipped', { reason: 'no_recent_data' });
    return { ok: true, skipped: true };
  }

  const findings = findingsRows.map(r => JSON.parse(r.value));
  const competitors = competitorRows.map(r => JSON.parse(r.value));

  const findingsSummary = findings.slice(0, 20).map(f => 
    `- [${f.topic}] ${f.title} (relevance: ${f.relevanceScore}/10)`
  ).join('\n');

  const competitorSummary = competitors.map(c =>
    `- ${c.competitor}: ${c.changes}`
  ).join('\n');

  const prompt = SYNTHESIS_PROMPT.replace(
    '{findings}',
    `News/Trends:\n${findingsSummary}\n\nCompetitor Intel:\n${competitorSummary}`
  );

  let brief = '';
  try {
    const response = await llmTool({ prompt, maxTokens: 1500 });
    brief = response.content;
  } catch (e) {
    if (logger) logger.error('synthesis_error', { error: e.message });
    return { ok: false, error: e.message };
  }

  // Write report
  const reportsDir = config.output?.reportsDir || '/tmp/research';
  const filename = `research-brief-${new Date().toISOString().slice(0, 10)}.md`;
  const reportPath = path.join(reportsDir, filename);

  const fullReport = `# Research Brief — ${new Date().toISOString().slice(0, 10)}

Generated by ResearchBot

---

${brief}

---

## Raw Data

### Findings (${findings.length} items)
${findingsSummary}

### Competitor Intel
${competitorSummary}

---
*Generated at ${new Date().toISOString()}*
`;

  try {
    await writeFileTool({ path: reportPath, content: fullReport });
    if (logger) logger.info('research_brief_written', { path: reportPath });
  } catch (e) {
    if (logger) logger.warn('report_write_error', { error: e.message });
  }

  // Store in DB
  db.run(
    `INSERT INTO kv_store (key, value, namespace, created_at)
     VALUES (:key, :value, 'research_briefs', datetime('now'))`,
    {
      key: `brief-${new Date().toISOString().slice(0, 10)}`,
      value: JSON.stringify({
        date: new Date().toISOString(),
        findingsCount: findings.length,
        competitorsCount: competitors.length,
        reportPath,
        brief
      })
    }
  );

  return { ok: true, reportPath, findingsCount: findings.length };
}

module.exports = { knowledgeSynthesisTask };
